    private void openItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_openItemActionPerformed
        // TODO add your handling code here:
        
        parPanel.removeAll();
        parPanel.add(txtEditor);
        parPanel.repaint();
        parPanel.revalidate();
        JFileChooser jfc=new JFileChooser();
        if(jfc.showOpenDialog(openItem)==JFileChooser.APPROVE_OPTION)
        {

            try {
                Scanner fin=new Scanner(jfc.getSelectedFile());
                String Buffer="";
                while(fin.hasNext())
                {
                    Buffer=Buffer+fin.nextLine()+"\n";
                    textEditor.setText(Buffer);

                }
            } catch (FileNotFoundException ex) {
                JOptionPane.showMessageDialog(this,"File Not Found\n");
            }
        }
    }//GEN-LAST:event_openItemActionPerformed

